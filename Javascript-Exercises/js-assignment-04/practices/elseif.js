// In JavaScript, if...else statement is like a choice-maker.
// It checks a condition, if the condition is true, it runs one set of instructions.
// If the condition is false, it runs a different set of instructions.

// if else statement is used when we have only two possibilities.

let age = +prompt("Please enter your Marks")
if (age >= 18) {
  alert("You are Allow to Drive")
} else {
  alert("Age is under 18")
  alert("You are Not Allow to Drive")
}

// If, The if statement is true it will execute the if body, otherwise it will execute the second part.
// You can avail this checker with two if statements but it will check both statements to see if they're true.


